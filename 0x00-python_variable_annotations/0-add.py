#!/usr/bin/env python3
def add(a: float, b: float) -> float:
    """Adds two numbers together.

    This function takes in two numbers (a and b) as inputs and returns
    their sum.

    Parameters
    ----------
    a : float
        The first number to add.
    b : float
        The second number to add.
ar

    Returns
    -------
    float
        The sum of a and b.

    """
    return a + b
