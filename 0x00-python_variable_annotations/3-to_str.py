def to_str(n: float) -> str:
    """Converts a floating-point number to a string.

    This function takes in a floating-point number (n) as input and
    returns a string representation of that number.

    Parameters
    ----------
    n : float
        The floating-point number to convert to a string.

    Returns
    -------
    str
        A string representation of n.

    """
    return str(n)
